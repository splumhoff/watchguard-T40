/* _sp_assembler_yylex_destroy is for both reentrant and non-reentrant scanners. */
int _sp_assembler_yylex_destroy  (void)
{
    
    /* Pop the buffer stack, destroying each element. */
	while(YY_CURRENT_BUFFER){
		_sp_assembler_yy_delete_buffer(YY_CURRENT_BUFFER  );
		YY_CURRENT_BUFFER_LVALUE = NULL;
		_sp_assembler_yypop_buffer_state();
	}

	/* Destroy the stack itself. */
	_sp_assembler_yyfree((yy_buffer_stack) );
	(yy_buffer_stack) = NULL;

    /* Reset the globals. This is important in a non-reentrant scanner so the next time
     * _sp_assembler_yylex() is called, initialization will occur. */
    yy_init_globals( );

    return 0;
}

